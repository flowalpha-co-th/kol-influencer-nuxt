export const useLanguageStore = defineStore('language', () => {
  const language = ref<Language>('th')

  function init() {
    if (import.meta.client) {
      const stored = localStorage.getItem('kol_language') as Language
      if (stored && (stored === 'th' || stored === 'en')) {
        language.value = stored
      }
    }
  }

  function setLanguage(lang: Language) {
    language.value = lang
    if (import.meta.client) {
      localStorage.setItem('kol_language', lang)
    }
  }

  function t(key: string): string {
    const translations = language.value === 'th' ? thTranslations : enTranslations
    const keys = key.split('.')
    let value: string | Translations = translations
    for (const k of keys) {
      if (typeof value === 'object' && value !== null && k in value) {
        value = value[k] as string | Translations
      } else {
        return key
      }
    }
    return typeof value === 'string' ? value : key
  }

  return { language, init, setLanguage, t }
})
